#include "../../headers/minishell.h"

int	echo_bi(char** args)
{
	if (args[0] == '-' && args[1] && args[1] == 'n')
	{
		args += 2;
		printf ("%s", args);
	}
	else
	{
	printf ("%s\n", args);
	}
	return (0);
}
